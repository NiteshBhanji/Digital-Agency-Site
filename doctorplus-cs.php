                        <ol class="overview-desc-list">
                            <li><span>Identify user personas:</span> The first step in designing an effective UX strategy is to understand who will be using the dashboard. Identify the key user personas, such as healthcare administrators, doctors, nurses, and support staff, and tailor the dashboard to meet their specific needs.</li>
                            <li><span>Keep it simple:</span> A dashboard that tracks the number of available beds for COVID patients needs to be easy to read and understand at a glance. Use simple and clear language and graphics that are easy to interpret quickly.</li>
                            <li><span>Highlight critical data:</span> Ensure that critical data such as the total number of available beds, the number of beds in use, and the number of patients waiting for a bed are prominently displayed. Use color-coded visual aids to highlight areas of concern or where action is needed.</li>
                            <li><span>Prioritize information:</span> Prioritize the information on the dashboard based on the needs of the user personas. For example, administrators may need to see the number of available beds across all facilities, while nurses may need to see the number of beds available in specific units.</li>
                            <li><span>Provide drill-down options:</span> Users may need to drill down into the data to see more details about a specific unit or facility. Provide options for users to filter the data, search for specific information, and sort data based on different criteria.</li>
                            <li><span>Wireframes & User flows:</span> Based on the documents we were provided with, weâ€™ve turned the list of features and requirements into user flows & wireframes to validate the logic behind the flow. Having reiterated a few times to address some of the initial design issues, weâ€™ve come to a deliverable that was ready for early usability testing.</li>
                            <li><span>Start with low-fidelity sketches:</span> Use a pen and paper to create quick, low-fidelity sketches of the design UX. We Don't worry about creating detailed drawings or making everything look perfect. The goal is to quickly explore different ideas and iterate over them.</li>
                            <li><span>Iterate and refine:</span> As we create each sketch, we consider what works well and what could be improved. Using these insights to iterate on the design, making refinements and adjustments to improve the overall UX.</li>
